/**----------------------------------------------------
 *       Map - может использовать в качестве ключа 
 *                         Object
 * ----------------------------------------------------
 */

let john = {name: 'John'};

// сохраняем количество посещений для каждого 
// пользователя

let visitsCountPeople = new Map();

// используем объект как ключ записывая посещения
