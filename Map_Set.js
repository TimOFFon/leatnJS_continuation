/**
 *    Object.fromEntries(obj) - делает противоположное,
 *                           получив массив пар ключ-
 *                           значение, создает из них
 *                           объект:
 */

let basket = Object.fromEntries([
    ['banana', 1],
    ['orange', 2],
    ['meat', 4],
]);

// console.log(basket); // {banana: 1, orange: 2, meat: 4}
// console.log(basket.orange); // 2 (стр. 274)




